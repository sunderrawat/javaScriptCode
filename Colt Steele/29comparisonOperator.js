// comparision operators are used to compare two values and return boolean true or false
//  >  greater than
// < less than
// >=  greater than or equal to
// <=  less than or equal to
// ==  equal to
// !=  not equal to
// === strict quality
// !== strict non equality

let num1 = 87
let num2 = 45
console.log(num1>num2)
console.log(num1<num2)
console.log(num1>=num2)
console.log(num1<=num2)
console.log(num1==num2)
console.log(num1!=num2)
console.log(num1===num2)
console.log(num1!==num2)